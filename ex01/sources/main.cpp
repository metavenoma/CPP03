#include "ScavTrap.hpp"

int	main(void)
{
	ScavTrap elf("Elf");
	ScavTrap orc("Orc");
	ScavTrap goblin(orc);

	orc.attack("Elf");
	elf.guardGate();
	elf.takeDamage(20);
	elf.attack("Orc");
	orc.takeDamage(20);
	orc.attack("Elf");
	elf.beRepaired(10);
	orc.beRepaired(7);
	goblin.attack("Elf");
	elf.takeDamage(20);
	return (0);
}
